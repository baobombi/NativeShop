class Product {
    constructor(id, ownerId, title, imageUrl, description, price, likeTotal) {
            this.id = id,
            this.ownerId = ownerId,
            this.imageUrl = imageUrl,
            this.description = description,
            this.price = price,
            this.title = title,
            this.likeTotal = likeTotal
    }
}
export default Product;